def pop_element_from_set():
    # Initialize my_set
    # output: Set of unique elements
    my_set = set("HelloWorld")
    print (my_set)

    # pop an element
    # output: random element
    print(my_set.pop())

    # clear my_set
    # output: set()
    my_set.clear()
    print(my_set)

pop_element_from_set()